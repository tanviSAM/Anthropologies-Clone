//Sorting by Price

function PriceSort(){
    var select= document.querySelector("#priceSort").value;
    console.log(select);

    if (select == "high"){
        prodData.sort(function (a, b) {
            return Number(b.p) - Number(a.p);

        });
    }
    if(select == "low"){
        prodData.sort(function (a, b) {
            return Number(a.p) - Number(b.p);

        });
    }
    // console.log(product);
    clothingData(prodData);
}

//Clothing Page Data

function clothingData(prodData){
    document.querySelector("#ProductDisplay").textContent ="";
    prodData.map(function (elem){
    
        var div1  = document.createElement('div');
        
        //Product Display
        var imagediv  = document.createElement('div');
        var img = document.createElement('img');
        img.setAttribute('src',elem.image_url);
        img.setAttribute('id','Product')
        imagediv.append(img);
    
        //Product Details
        var namediv  = document.createElement('div');
        var namehead = document.createElement('p');
        namehead.textContent = elem.name;
        namediv.append(namehead);
        
        var rate  = document.createElement('p');
        var mrpdiv  = document.createElement('div');
        rate.textContent = elem.price;
        mrpdiv.append(rate);
        
        var button = document.createElement('button');
        button.setAttribute('id','btn');
        button.textContent = "Quick Pick";
        button.addEventListener("click", function(){
            addCart(elem);
        });
        
        
        div1.append(imagediv,namediv,mrpdiv,button); 
        document.querySelector('#ProductDisplay').append(div1);
        
    })
}

